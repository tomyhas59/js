const date = new Date();

console.log(date);

console.log(date.getFullYear()); //2023
console.log(date.getDate()); // 현재 날짜
console.log(date.getHours()); //현재 시간
console.log(date.getMonth()); //현재 달-1
console.log(date.getMinutes()); //현재 분
console.log(date.getSeconds()); //현재 초
console.log(date.toLocaleString()); //한글로 표시
